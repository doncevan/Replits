package ReplitPractice;

public class R061_ForLoop {
    public static void main(String[] args) {
        //Write a program that uses for loop to prints multiplication of  number 3 from 1 to 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(3 + "*" + i + "=" + i * 3);
        }
    }
}







