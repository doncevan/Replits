package ReplitPractice;

public class R009_Multiplication {
    public static void main(String[] args) {
        /* Create a float data type variable and assign a value of  8.2 to it. Multiply the value by itself and print the result.
               The multiplication of ___ with itself is equal to ____   */
        float num = 8.2F;
        float sum = num * 8.2F;
        System.out.println("The multiplication of " + num + " with itself is equal to " + sum);
    }
}
