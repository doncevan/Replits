package ReplitPractice;

public class R010_ArithmeticOps {
    public static void main(String[] args) {
        /* 1. Declare 2 numbers and assign values 200 and 100 respectively.
           2. First, add numbers, then subtract, third multiply, forth divide.
           3. Please use variables to print the values of each operation result on a separate lines.
                  300;         100;       20000;         2  */
        int number1 = 200;
        int number2 = 100;
        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number1 / number2);
    }
}
