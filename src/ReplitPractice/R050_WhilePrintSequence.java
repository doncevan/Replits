package ReplitPractice;

public class R050_WhilePrintSequence {
    public static void main(String[] args) {
        /* Step 1: Initialize the i value as int i=7. Step 2: Write a program to print a sequence of numbers starting from 7 to 98. */
        int i = 7;
        while (i <= 98) {
            System.out.print(i+" ");
            i += 7;
        }
    }
}
