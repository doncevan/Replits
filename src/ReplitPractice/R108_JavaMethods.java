package ReplitPractice;
/*
create method and name as m1. Inside your method body add print statement as "m1
method implementation"
create another method and name as m2. Inside your method body add print statement
as "m2 method implementation"
Call these two methods in the main method  */
public class R108_JavaMethods {
    static void m1() {
        System.out.println("m1 method implementation");
    }

    static void m2() {
        System.out.println("m2 method implementation");
    }

    public static void main(String[] args) {
        m1();
        m2();
    }
}